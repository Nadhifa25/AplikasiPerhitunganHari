    // Fungsi untuk menghitung jumlah hari dalam bulan dan tahun tertentu
private int getJumlahHari(int bulan, int tahun) {
    switch(bulan) {
        case 1: case 3: case 5: case 7: case 8: case 10: case 12: // Bulan dengan 31 hari
            return 31;
        case 4: case 6: case 9: case 11: // Bulan dengan 30 hari
            return 30;
        case 2: // Februari
            if (isLeapYear(tahun)) {
                return 29;  // Tahun kabisat
            } else {
                return 28;  // Tahun biasa
            }
        default:
            return 0; // Invalid
    }
}

// Fungsi untuk mengecek apakah tahun adalah tahun kabisat
private boolean isLeapYear(int tahun) {
    return (tahun % 4 == 0 && (tahun % 100 != 0 || tahun % 400 == 0));
}

// Fungsi untuk mendapatkan hari pertama bulan tertentu
private String getHariPertama(int bulan, int tahun) {
    LocalDate firstDay = LocalDate.of(tahun, bulan, 1);
    return firstDay.getDayOfWeek().toString();
}

// Fungsi untuk mendapatkan hari terakhir bulan tertentu
private String getHariTerakhir(int bulan, int tahun) {
    LocalDate lastDay = LocalDate.of(tahun, bulan, getJumlahHari(bulan, tahun));
    return lastDay.getDayOfWeek().toString();
}
    
