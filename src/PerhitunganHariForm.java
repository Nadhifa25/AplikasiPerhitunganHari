    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
       // Ambil bulan dan tahun yang dipilih
    int bulan = bulanComboBox.getSelectedIndex() + 1;  // Kombinasi bulan 1-12
    int tahun = (int) tahunSpinner.getValue();

    // Hitung jumlah hari dalam bulan tersebut
    int jumlahHari = getJumlahHari(bulan, tahun);
    
    // Menampilkan hasil
    hasilLabel.setText("Jumlah hari di bulan " + bulanComboBox.getSelectedItem() + " tahun " + tahun + ": " + jumlahHari + " hari");
    
    // Menampilkan hari pertama dan terakhir dalam bulan tersebut
    String hariPertama = getHariPertama(bulan, tahun);
    String hariTerakhir = getHariTerakhir(bulan, tahun);
    informasiLabel.setText("Hari pertama: " + hariPertama + ", Hari terakhir: " + hariTerakhir);
    }//GEN-LAST:event_btnHitungActionPerformed

